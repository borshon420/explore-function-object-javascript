var color = 'white';

switch (color) {
    case 'blue':
       console.log('color is blue');
       break;
    case 'red':
        console.log('color is red');
        break;
    case 'white':
        console.log('color is white');
        break;
    case 'green':
        console.log('color is greem');
        break;
    default:
        console.log('color is black');
}